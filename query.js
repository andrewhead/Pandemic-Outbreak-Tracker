function getCases(data, date, disease, country) {
    if (!data.cases[disease][date].hasOwnProperty(country)) {
        return 0;
    } else {
        return data.cases[disease][date][country];
    }
}

function getFlights(data, arrCountry, depCountry) {
    /* This method may be useless to us given our current data. */
    if (!data.flights.hasOwnProperty(arrCountry) ||
        !data.flights[arrCountry].hasOwnProperty(depCountry)) {
        return 0;
    } else {
        return data.flights[arrCountry][depCountry];
    }
}

function hasFlightRisk(data, date, disease, country, minRoutes, minCases) {
    /*
    Test:
    var data = {
        "cases": {
            "ebola": {
                "20140101": {
                    "000": 0,
                    "111": 1,
                    "222": 0,
                    "333": 2
                }
            }
        },
        "flights": {
            "000": {
                "111": 0,
                "222": 1,
                "333": 2
            }
        }
    };
    hasFlightRisk(data, "20140101", "ebola", "000", 1, 1); // true
    hasFlightRisk(data, "20140101", "ebola", "000", 3, 1); // false
    hasFlightRisk(data, "20140101", "ebola", "000", 1, 3); // false
    hasFlightRisk(data, "20140101", "ebola", "444", 1, 3); // false
    */
    if (!data.flights.hasOwnProperty(country)) {
        return false;
    }
    var riskyNeighbor = false;
    for (var adjCountry in data.flights[country]) {
        if (data.flights[country].hasOwnProperty(adjCountry)) {
            if ((getCases(data, date, disease, adjCountry) >= minCases) &&
                data.flights[country][adjCountry] > minRoutes) {
                riskyNeighbor = true;
                break;
            }
        }
    }
    return riskyNeighbor;
}

function hasBorderRisk(data, date, disease, country, minCases) {
    /*
    Test:
    var data = {
        "cases": {
            "ebola": {
                "20140101": {
                    "000": 0,
                    "111": 0,
                    "222": 2,
                }
            }
        },
        "borders": {
            "000": [
                "111"
            ],
            "111": [
                "000",
                "222"
            ]
        }
    };
    hasBorderRisk(data, "20140101", "ebola", "000", 1); // false
    hasBorderRisk(data, "20140101", "ebola", "111", 1); // true
    hasBorderRisk(data, "20140101", "ebola", "111", 3); // false
    hasBorderRisk(data, "20140101", "ebola", "444", 1); // false
    */
    if (!data.borders.hasOwnProperty(country)) {
        return false;
    }
    var adjCountries = data.borders[country];
    var riskyNeighbor = false;
    for (var i = 0; i < adjCountries.length; i++) {
        var adjCountry = adjCountries[i];
        if ((getCases(data, date, disease, adjCountry) >= minCases)) {
            riskyNeighbor = true;
            break;
        }
    }
    return riskyNeighbor;
}

function getRiskCountries(
    disease,
    date,
    minLifeExpectancy,
    maxLifeExpectancy,
    minRoutesInto,
    considerBorderRisk,
    minLocalCases,
    borderingCases
    ) {
    /* 
    Sanity tests for the future (assuming these results were correct to begin with):
    getRiskCountries("flu", "20141005", 75, 86, 10, false, 10000, 1000).length  // 27
    getRiskCountries("flu", "20141005", 40, 86, 1, true, 10000, 1000).length  // 114
    getRiskCountries("ebola", "20140928", 40, 86, 10, true, 1, 100).length  // 8
    */
    var data = getData();
    var riskCountries = [];
    /* Start by considering all countries */
    for (var country in data.lifeExpectancies) {
        if (data.lifeExpectancies.hasOwnProperty(country)) {
            /* Only consider countries within life expectancy range */
            var lifeExpectancy = data.lifeExpectancies[country];
            if ((lifeExpectancy < minLifeExpectancy) || (lifeExpectancy > maxLifeExpectancy)) {
                continue;
            }
            /* Risk can be produced by local or bordering outbreaks. */
            if (getCases(data, date, disease, country) > minLocalCases ||
                hasFlightRisk(data, date, disease, country, minRoutesInto, borderingCases) ||
                (considerBorderRisk && hasBorderRisk(data, date, disease, country, borderingCases))) {
                riskCountries.push(country);
            }
        }
    }
    return riskCountries;
}

